-- É possibile ottenere informazioni sulle classi di tipo con :info.

-- Eq è la classe di tipi degli insiemi con uguaglianza.

-- Ord è la classe di tipi degli insiemi con ordinamento.

-- Enum è la classe di tipi degli insiemi con ordinamento e successore.

-- Bounded è la classe di tipi degli insiemi con un massimo e un minimo.

-- Num è la classe di tipi degli insiemi numerici.
-- Tutti i numeri hanno le operazioni +, -, * e abs.
-- Integral è la classe di tipi degli insiemi numerici interi.
-- Su Integral sono definite le operazioni div e mod.
-- Fractional è la classe di tipi degli insiemi numerici frazionari.
-- Su Fractional è definita l'operazione /.
-- RealFrac è la classe di tipi degli insiemi numerici frazionari con approssimazione.
-- Su RealFrac sono definite le funzioni properFraction, truncate, round e ceiling.
-- Floating è la classe di tipi degli insiemi numerici reali.
-- Su Floating sono definite le funzioni exp, log, sqrt, ** e tutte le funzioni trigonometriche.